void Usart_SendStriong(USART_TypeDef* USARTx, char *str)
{
	while(*str)
	{
		while(!USART_GetFlagStatus(USARTx, USART_FLAG_TXE));
		USART_SendData(USARTx, *str);
		while(USART_GetFlagStatus(USARTx, USART_FLAG_TC));
		str++;
	}
}

 



